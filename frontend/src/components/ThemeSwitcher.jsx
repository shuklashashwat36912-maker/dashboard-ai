function ThemeSwitcher() {
  const { currentTheme, setTheme } = useTheme();
  const [isOpen, setIsOpen] = useState(false);

  const currentThemeData = themes[currentTheme];

  return (
    <div className="theme-switcher">
      <button 
        className="theme-toggle-btn" 
        onClick={() => setIsOpen(!isOpen)}
        title="Change Theme"
      >
        <ThemeIcon icon="palette" size={20} />
        <span className="theme-name">{currentThemeData.name}</span>
        <svg 
          className={`chevron ${isOpen ? 'open' : ''}`} 
          width="16" 
          height="16" 
          viewBox="0 0 24 24" 
          fill="none" 
          stroke="currentColor" 
          strokeWidth="2" 
          strokeLinecap="round" 
          strokeLinejoin="round"
        >
          <polyline points="6 9 12 15 18 9"/>
        </svg>
      </button>

      {isOpen && (
        <>
          <div className="theme-overlay" onClick={() => setIsOpen(false)} />
          <div className="theme-dropdown">
            <div className="theme-dropdown-header">
              <span>Select Theme</span>
            </div>
            <div className="theme-options">
              {Object.entries(themes).map(([key, theme]) => (
                <button
                  key={key}
                  className={`theme-option ${currentTheme === key ? 'active' : ''}`}
                  onClick={() => {
                    setTheme(key);
                    setIsOpen(false);
                  }}
                >
                  <div 
                    className="theme-preview"
                    style={{ 
                      background: theme.colors.bgCard,
                      borderColor: theme.colors.border
                    }}
                  >
                    <div 
                      className="theme-preview-accent"
                      style={{ background: theme.colors.primary }}
                    />
                  </div>
                  <div className="theme-info">
                    <span className="theme-option-name">{theme.name}</span>
                  </div>
                  <div 
                    className="theme-icon-wrapper"
                    style={{ color: theme.colors.primary }}
                  >
                    <ThemeIcon icon={theme.icon} size={16} />
                  </div>
                  {currentTheme === key && (
                    <svg className="check-icon" width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2.5" strokeLinecap="round" strokeLinejoin="round">
                      <polyline points="20 6 9 17 4 12"/>
                    </svg>
                  )}
                </button>
              ))}
            </div>
          </div>
        </>
      )}
    </div>
  );
}

export default ThemeSwitcher;
